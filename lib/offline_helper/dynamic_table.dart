import 'package:hive/hive.dart';

part 'dynamic_table.g.dart';

@HiveType(typeId: 0)
class OrigoDynamicTable extends HiveObject {
  @HiveField(0)
  late int status;

  @HiveField(1)
  late String message;

  @HiveField(2)
  late List<dynamic> result;

  OrigoDynamicTable(
      {required this.status, required this.message, required this.result});
}

@HiveType(typeId: 1)
class OrigoMapDynamicTable extends HiveObject {
  @HiveField(0)
  late int status;

  @HiveField(1)
  late String message;

  @HiveField(2)
  late Map<String, dynamic> result;

  OrigoMapDynamicTable(
      {required this.status, required this.message, required this.result});
}
